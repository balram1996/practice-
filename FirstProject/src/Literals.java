public class Literals {
    public static void main(String[] args) {
        int age = 34;
        byte myAge = 26;
        short num1 = 87; //it will not take long value like 5777777 etc.
        long num = 566666666L; //for variable long we have to add L/l otherwise it will consider default int and through error.
        char ch = 'a';
        float f1 = 5.6f;
        double d1 = 5.66; //java take double as default no need to add d but if we want we can add 5.66d
        boolean a = true;
        String name = "balram";
        System.out.println(name);
    }
}
